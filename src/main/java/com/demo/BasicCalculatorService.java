package com.demo;
public class BasicCalculatorService {

    public int doSum(int i,int j){
        return i+j;
    }

    public int doDiff(int i,int j){
        return i-j;
    }

    public int multiply(int i,int j){
        return i*j;
    }

    public int divide(int i,int j)
    {
        return i/j;
    }
}
